    private void btnCetakPendudukPindahDatangMatiActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnCetakPendudukPindahDatangMatiActionPerformed
        // TODO add your handling code here:
        try{
                        JasperDesign jasperDesign = JRXmlLoader.load(LaporanView.class.getResourceAsStream("/com/uwika/laporan/laporan_penduduk_mutasi.jrxml"));
	            preparedStatement = KoneksiMySQL.getConnection().prepareStatement("select d.namaLengkap, d.nik, m.keterangan from data_penduduk d inner join mutasi m on d.nik = m.nik where MONTH(m.tanggal) =  " + (comboBoxBulan.getSelectedIndex() + 1) + " AND YEAR(m.tanggal) = " + comboBoxTahun.getSelectedItem().toString() );

	            HashMap<String,Object> param = new HashMap<String,Object>();
                    param.put("kepalaDesa", txtKepalaDesaSugeng.getText());
                    param.put("bulan", comboBoxBulan.getSelectedItem().toString().toUpperCase());
                    param.put("tahun", comboBoxTahun.getSelectedItem().toString().toUpperCase());
	            JRDesignQuery newQuery = new JRDesignQuery();
	            newQuery.setText(preparedStatement.toString().split("\\:")[1]);
	            jasperDesign.setQuery(newQuery);
	            JasperReport jasperReport = JasperCompileManager.compileReport(jasperDesign);
	            JasperPrint jasperPrint = JasperFillManager.fillReport(jasperReport,param, KoneksiMySQL.getConnection());
	            JasperViewer.viewReport(jasperPrint,false);
        }catch (Exception e){
            e.printStackTrace();
            JOptionPane.showMessageDialog(null, e);
        }
    }//GEN-LAST:event_btnCetakPendudukPindahDatangMatiActionPerformed

